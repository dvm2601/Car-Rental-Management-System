public class car_details {
    int Car_id;
    String Car_Names;
    String No_plate;
    String Brand;
    String Car_Type;
    String Fuel_Type;
    String perday_price_without_driver;
    String perday_price_with_driver;
    String Security;
    String description;
    String Photo;
    public car_details(int Car_id, String Car_Names, String No_plate,
    String Brand,
    String Car_Type,
    String Fuel_Type,
    String perday_price_without_driver,
    String perday_price_with_driver,
    String Security,
    String description,
    String Photo) {
        this.Car_id = Car_id;
        this.Car_Names = Car_Names;
        this.No_plate = No_plate;
        this.Brand = Brand;
        this.Car_Type = Car_Type;
        this.Fuel_Type = Fuel_Type;
        this.perday_price_without_driver = perday_price_without_driver;
        this.perday_price_with_driver = perday_price_with_driver;
        this.Security = Security;
        this.description = description;
        this.Photo = Photo;



    }

}


