    public UserBookingHistory() {
        try {
            al.clear();
            ResultSet rs = DBLoader.executeQuery("select * from booking where email = '"+global.uname+"'");
            while (rs.next()) {
                int booking_id = rs.getInt("booking_id");
                int car_id = rs.getInt("Car_id");
                String price_per_day = rs.getString("price_per_day");
                String start_date = rs.getString("start_date");
                String end_date = rs.getString("end_date");
                String no_of_days = rs.getString("no_of_days");
                String rent = rs.getString("rent");
                String security = rs.getString("security");
                String name = rs.getString("name");
                String mobile_no = rs.getString("mobile_no");
                String address = rs.getString("address");
                String email = rs.getString("email");
                String payment_status = rs.getString("payment_status");
                String booking_status = rs.getString("booking_status");
                al.add(new UserBookingData(booking_id, car_id, price_per_day, start_date, end_date, no_of_days, rent, security, name, mobile_no, address, email, payment_status, booking_status));

            }
            initComponents();
            tm = new mytablemodel();
            setSize(1600,900);
            setExtendedState(MAXIMIZED_BOTH);
            setLocationRelativeTo(null);
            jt1.setModel(tm);
            tm.fireTableDataChanged();

        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

