    class mytablemodel extends AbstractTableModel {

        @Override
        public int getRowCount() {
            return al.size();
        }

        @Override
        public int getColumnCount() {
            return 14;
        }

        @Override
        public Object getValueAt(int i, int j) {
            UserBookingData a = al.get(i);
            if(j==0)
            {
                return a.booking_id;
            }
            else if(j==1)
            {
                return a.car_id;
            }
            else if(j==2)
            {
                return a.price_per_day;
            }
            else if(j==3)
            {
                return a.start_date;
            }
            else if(j==4)
            {
                return a.end_date;
            }
            else if(j==5)
            {
                return a.no_of_days;
            }
            else if(j==6)
            {
                return a.rent;
            }
            else if(j==7)
            {
                return a.security;
            }
            else if(j==8)
            {
                return a.name;
            }
            else if(j==9)
            {
                return a.mobile_no;
            }
            else if(j==10)
            {
                return a.address;
            }
            else if(j==11)
            {
                return a.email;
            }
            else if(j==12)
            {
                return a.payment_status;
            }
            else{
                return a.booking_status;
            }
        }

        public String getColumnName(int column) {
            String name[] = {"booking_id","car_id","price_per_day","start_date","end_date","no_of_days","rent","security","name","mobile_no","address","email","payment_status","booking_status"};
            return name[column];
        }
    }
