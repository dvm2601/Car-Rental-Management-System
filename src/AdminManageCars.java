    class mytablemodel extends AbstractTableModel {

        @Override
        public int getRowCount() {
            return al.size();
        }

        @Override
        public int getColumnCount() {
            return 11;
        }

        @Override
        public Object getValueAt(int i, int j) {
            car_details c = (car_details) al.get(i);
            if (j == 0) {
                return c.Car_id;
            } else if (j == 1) {
                return c.Car_Names;
            } else if (j == 2) {
                return c.No_plate;
            } else if (j == 3) {
                return c.Brand;
            } else if (j == 4) {
                return c.Car_Type;
            } else if (j == 5) {
                return c.Fuel_Type;
            } else if (j == 6) {
                return c.perday_price_with_driver;
            } else if (j == 7) {
                return c.perday_price_without_driver;
            } else if (j == 8) {
                return c.Security;
            } else if (j == 9) {
                return c.description;
            } else {
                return c.Photo;
            }
        }

        public String getColumnName(int column) {
            String name[] = {"Car_id", "Car_Names", "No_plate", "Brand", "Car_Type", "Fuel_Type", "perday_price_without_driver", "perday_price_with_driver", "Security", "description", "Photo"};
            return name[column];
        }
    }

