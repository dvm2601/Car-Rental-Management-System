    private void bt2ActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        int del = jt1.getSelectedRow();
        try {
            int lo = JOptionPane.showConfirmDialog(this, "Are you sure you want to remove this car?");
            if (lo == JOptionPane.YES_OPTION) {

                ResultSet rs = DBLoader.executeQuery("select * from car_details ");
                rs.absolute(del + 1);
                rs.deleteRow();
                al.remove(del);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        tm.fireTableDataChanged();
    }


