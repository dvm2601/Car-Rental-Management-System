    private void bt2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt2ActionPerformed
        // TODO add your handling code here:
        try {
            ResultSet rs = DBLoader.executeQuery("select * from booking");
            if (tf4.getText().equals(global.uname)) {

                rs.moveToInsertRow();
                rs.updateString("car_id", lb1.getText());
                rs.updateString("price_per_day", lb2.getText());
                rs.updateString("start_date", lb3.getText());
                rs.updateString("end_date", lb4.getText());
                rs.updateString("no_of_days", lb5.getText());
                rs.updateString("rent", lb6.getText());
                rs.updateString("security", lb8.getText());
                rs.updateString("name", tf1.getText());
                rs.updateString("mobile_no", tf2.getText());
                rs.updateString("address", tf3.getText());
                rs.updateString("email", tf4.getText());
                rs.updateString("payment_status", global.payment_status);
                rs.updateString("booking_status", "success");
                rs.insertRow();
                tf1.setText("");
                tf2.setText("");
                tf3.setText("");
                tf4.setText("");
                JOptionPane.showMessageDialog(null, "Booking Successful");
                UserBookingHistory bookingHistory = new UserBookingHistory();
                bookingHistory.setVisible(true);

            } else {
                JOptionPane.showMessageDialog(null, "Invalid email!");
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_bt2ActionPerformed

