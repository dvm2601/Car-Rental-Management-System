    public Submission(String carid, String rentperday, String from, String to, int days) {

        initComponents();
        setSize(1600,900);
        try {
            ResultSet rs = DBLoader.executeQuery("Select * from car_details where Car_id='" + carid + "'");
            if (rs.next()) {
                String security = rs.getString("security");
                int rent = Integer.parseInt(rentperday) * days;
                int sec = Integer.parseInt(security);
                String total_rent = (rent + sec) + "";

                this.total_rent = total_rent;
                lb1.setText(carid);
                lb2.setText(rentperday);
                lb3.setText(from);
                lb4.setText(to);

                if (days == 0) {
                    days = 1;
                    rent = Integer.parseInt(rentperday) * days;
                    lb5.setText(days+"");
                    lb6.setText(rent + "");
                    total_rent = (rent + sec) + "";
                    this.total_rent = total_rent;


                } else {
                    lb5.setText(days + "");
                    lb6.setText(rent + "");
                }

                lb8.setText(security);
                lb7.setText(total_rent);

            } else {
                System.out.println("error");
            }

        } catch (Exception ex) {
            ex.printStackTrace();
        }
        setSize(1492, 759);
        setExtendedState(MAXIMIZED_BOTH);
        setLocationRelativeTo(null);
    }

